  CONSTRAINT FK_SESMT_EVID_REGISTRO FOREIGN KEY (REGISTRO_ID) REFERENCES SESMT_REGISTRO (ID),
  CONSTRAINT FK_SESMT_EVID_ANEXO FOREIGN KEY (ANEXO_ID) REFERENCES SESMT_REGISTRO_ANEXO (ID)
);

CREATE INDEX IDX_SESMT_REG_MODULO ON SESMT_REGISTRO (MODULE_KEY, UNIDADE_CODIGO, STATUS);
