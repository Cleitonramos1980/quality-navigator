-- SESMT / SST core schema for Quality Navigator
-- Date: 2026-03-27

CREATE TABLE SESMT_UNIDADE (
  ID VARCHAR2(36) PRIMARY KEY,
  CODIGO VARCHAR2(10) NOT NULL,
  NOME VARCHAR2(120) NOT NULL,
  REGIONAL VARCHAR2(80),
  ATIVO NUMBER(1) DEFAULT 1 NOT NULL,
  CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  UPDATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL
);

CREATE TABLE SESMT_SETOR (
  ID VARCHAR2(36) PRIMARY KEY,
  UNIDADE_ID VARCHAR2(36) NOT NULL,
  NOME VARCHAR2(120) NOT NULL,
  RESPONSAVEL VARCHAR2(120),
  CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  UPDATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT FK_SESMT_SETOR_UNIDADE FOREIGN KEY (UNIDADE_ID) REFERENCES SESMT_UNIDADE (ID)
);

CREATE TABLE SESMT_FUNCAO (
  ID VARCHAR2(36) PRIMARY KEY,
  CODIGO VARCHAR2(30),
  NOME VARCHAR2(120) NOT NULL,
  CARGO VARCHAR2(120),
  CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  UPDATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL
);

CREATE TABLE SESMT_COLABORADOR (
  ID VARCHAR2(36) PRIMARY KEY,
  MATRICULA VARCHAR2(40),
  NOME VARCHAR2(140) NOT NULL,
  UNIDADE_ID VARCHAR2(36) NOT NULL,
  SETOR_ID VARCHAR2(36),
  FUNCAO_ID VARCHAR2(36),
  STATUS VARCHAR2(20) DEFAULT 'ATIVO' NOT NULL,
  CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  UPDATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT FK_SESMT_COLAB_UNIDADE FOREIGN KEY (UNIDADE_ID) REFERENCES SESMT_UNIDADE (ID),
  CONSTRAINT FK_SESMT_COLAB_SETOR FOREIGN KEY (SETOR_ID) REFERENCES SESMT_SETOR (ID),
  CONSTRAINT FK_SESMT_COLAB_FUNCAO FOREIGN KEY (FUNCAO_ID) REFERENCES SESMT_FUNCAO (ID)
);

CREATE TABLE SESMT_PROFISSIONAL (
  ID VARCHAR2(36) PRIMARY KEY,
