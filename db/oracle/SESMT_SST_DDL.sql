CREATE TABLE SESMT_REGISTRO_EVIDENCIA (
  ID VARCHAR2(36) PRIMARY KEY,
  REGISTRO_ID VARCHAR2(36) NOT NULL,
  TIPO VARCHAR2(60),
  DESCRICAO VARCHAR2(1000),
  DATA_EVIDENCIA DATE,
  RESPONSAVEL VARCHAR2(120),
